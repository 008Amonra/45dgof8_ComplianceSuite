# ── Deploy n8n ────────────────────────────────────────────────
deploy_n8n() {
  header "Deploying n8n (Workflow Engine)"

  if [ -f "$N8N_DIR/docker-compose.yml" ]; then
    warn "n8n already configured. Skipping."
    return
  fi

  cat > "$N8N_DIR/docker-compose.yml" <<EOF
services:
  n8n:
    image: docker.n8n.io/n8nio/n8n:latest
    container_name: n8n-local
    restart: unless-stopped
    ports:
      - "$N8N_PORT:5678"
    environment:
      - N8N_HOST=localhost:$N8N_PORT
      - N8N_PROTOCOL=http
      - N8N_PORT=5678
      - N8N_METRICS=false
      - WEBHOOK_URL=http://localhost:$N8N_PORT/
      - N8N_ENCRYPTION_KEY=$(openssl rand -hex 24)
      - N8N_USER_FOLDER=/home/node/.n8n
      - GENERIC_TIMEZONE=UTC
    volumes:
      - n8n_data:/home/node/.n8n
volumes:
  n8n_data:
EOF

  # Check if port is free
  if ss -tln 2>/dev/null | grep -q ":$N8N_PORT "; then
    warn "Port $N8N_PORT already in use. n8n may already be running."
  fi

  info "Starting n8n..."
  cd "$N8N_DIR" && run_docker "docker compose" docker compose up -d

  # Wait for n8n to be ready (up to 60s)
  info "Waiting for n8n to be ready..."
  local wait=0
  while [ $wait -lt 60 ]; do
    if curl -s -o /dev/null -w "%{http_code}" "http://localhost:$N8N_PORT/healthz" 2>/dev/null | grep -q 200; then
      ok "n8n running at http://localhost:$N8N_PORT"
      break
    fi
    sleep 2
    wait=$((wait+2))
  done
  if [ $wait -ge 60 ]; then
    err "n8n not responding after 60s. Check: run_docker docker logs n8n-local"
    exit 1
  fi
}

# ── Create Owner Account ──────────────────────────────────────
create_owner() {
  header "Creating n8n Owner Account"
  local cookie=$(mktemp)
  local user_resp=$(curl -s -c "$cookie" "http://localhost:$N8N_PORT/rest/owner/setup" \
    -H "Content-Type: application/json" \
    -d "{\"email\":\"$OWNER_EMAIL\",\"firstName\":\"Agent\",\"lastName\":\"Ops\",\"password\":\"changeme123\"}" 2>/dev/null)

  if echo "$user_resp" | grep -q "alreadySetup"; then
    warn "Owner already set up. Skipping."
  else
    ok "Owner account created: $OWNER_EMAIL / changeme123"
  fi
  rm -f "$cookie"
}

# ── Create MCP API Key ────────────────────────────────────────
create_mcp_key() {
  header "Creating n8n API Key for MCP"
  local mcp_key=""

  # Try to get existing key or create one
  local login_resp=$(curl -s -c /tmp/n8n-cookies.txt \
    "http://localhost:$N8N_PORT/rest/login" \
    -H "Content-Type: application/json" \
    -d "{\"email\":\"$OWNER_EMAIL\",\"password\":\"changeme123\"}")

  local api_resp=$(curl -s -b /tmp/n8n-cookies.txt \
    "http://localhost:$N8N_PORT/rest/api-keys" \
    -H "Content-Type: application/json")

  if echo "$api_resp" | python3 -c "import sys,json; d=json.load(sys.stdin); print(d.get('data',[{}])[0].get('apiKey',''))" 2>/dev/null | grep -q .; then
    mcp_key=$(python3 -c "import sys,json; d=json.load(sys.stdin); print(d.get('data',[{}])[0].get('apiKey',''))" <<< "$api_resp")
    ok "Using existing API key"
  else
    local create_resp=$(curl -s -b /tmp/n8n-cookies.txt -X POST \
      "http://localhost:$N8N_PORT/rest/api-keys" \
      -H "Content-Type: application/json" \
      -d '{"label":"opencode-mcp"}')
    mcp_key=$(python3 -c "import sys,json; print(json.load(sys.stdin).get('data',{}).get('apiKey',''))" <<< "$create_resp" 2>/dev/null)
    ok "API key created"
  fi

  echo "$mcp_key" > "$INSTALL_DIR/n8n-api-key.txt"
  info "API key saved to $INSTALL_DIR/n8n-api-key.txt"
}

# ── Install cloudflared Tunnel ────────────────────────────────
setup_tunnel() {
  header "Setting up Cloudflare Tunnel (HTTPS access)"

  if command -v cloudflared &>/dev/null; then
    ok "cloudflared already installed: $(cloudflared --version)"
  else
    info "Installing cloudflared..."
    curl -fsSL https://github.com/cloudflare/cloudflared/releases/latest/download/cloudflared-linux-amd64 -o /tmp/cloudflared
    chmod +x /tmp/cloudflared
    sudo mv /tmp/cloudflared /usr/local/bin/cloudflared
    ok "cloudflared installed"
  fi

  # Start tunnel
  local tunnel_log="$INSTALL_DIR/tunnel.log"
  nohup cloudflared tunnel --url "http://localhost:$N8N_PORT" > "$tunnel_log" 2>&1 &
  TUNNEL_PID=$!
  echo "$TUNNEL_PID" > "$INSTALL_DIR/tunnel.pid"
  sleep 4

  local tunnel_url=$(grep -oP 'https://[a-zA-Z0-9.-]+\.trycloudflare\.com' "$tunnel_log" 2>/dev/null | head -1)
  if [ -n "$tunnel_url" ]; then
    ok "Tunnel URL: $tunnel_url"
    echo "$tunnel_url" > "$INSTALL_DIR/tunnel-url.txt"
  else
    warn "Tunnel starting... check later: cat $tunnel_log"
  fi
}

