def check_port(p):
  with socket.socket(socket.AF_INET, socket.SOCK_STREAM) as s:
    return s.connect_ex(('localhost', p)) == 0

class Handler(http.server.BaseHTTPRequestHandler):
  def do_GET(self):
    if self.path == '/status':
      services = [
        ("n8n Workflow Engine", 5678, "http://localhost:5678"),
        ("Command Center", PORT, f"http://localhost:{PORT}"),
      ]
      cards = []
      for name, port, url in services:
        cards.append({"name": name, "ok": check_port(port), "url": url})
      self.send_response(200)
      self.send_header('Content-Type', 'application/json')
      self.send_header('Access-Control-Allow-Origin', '*')
      self.end_headers()
      self.wfile.write(json.dumps({"cards": cards}).encode())
    else:
      self.send_response(200)
      self.send_header('Content-Type', 'text/html')
      self.end_headers()
      self.wfile.write(HTML.encode())

http.server.HTTPServer(('0.0.0.0', PORT), Handler).serve_forever()
DASHEOF

  # Start dashboard
  local pid_file="$INSTALL_DIR/dashboard.pid"
  nohup python3 "$CMD_DIR/server.py" > "$INSTALL_DIR/dashboard.log" 2>&1 &
  echo $! > "$pid_file"
  sleep 1

  if ps -p $(cat "$pid_file") &>/dev/null; then
    ok "Dashboard running at http://localhost:$CMD_PORT"
  else
    warn "Dashboard may not have started. Check: cat $INSTALL_DIR/dashboard.log"
  fi
}

