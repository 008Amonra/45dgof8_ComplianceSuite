# ── Restore Point ─────────────────────────────────────────
RESTORE_DIR=""

# ── Docker Helper (handles permission issues automatically) ──
run_docker() {
  local desc="$1"
  shift
  if "$@" 2>/dev/null; then
    return 0
  fi
  warn "Docker permission issue. Trying sg docker..."
  if sg docker -c "$*" 2>/dev/null; then
    return 0
  fi
  warn "Trying with sudo..."
  if sudo "$@" 2>/dev/null; then
    return 0
  fi
  err "Cannot run: $*"
  err "Try: sudo usermod -aG docker $USER && log out and back in"
  exit 1
}


create_restore_point() {
  local ts=$(date +%Y%m%d-%H%M%S)
  RESTORE_DIR="$INSTALL_DIR/restore/$ts"
  mkdir -p "$RESTORE_DIR"
  
  header "Creating Restore Point"
  echo -e "  ${CYAN}Backup:${NC} $RESTORE_DIR"

  # Save current crontab
  crontab -l 2>/dev/null > "$RESTORE_DIR/crontab.before" || true

  # Save docker state
  docker ps --format '{{.Names}}' 2>/dev/null > "$RESTORE_DIR/containers.before" || true

  # Save cloudflared config if exists
  [ -f $HOME/.cloudflared/config.yml ] && cp $HOME/.cloudflared/config.yml "$RESTORE_DIR/cloudflared-config.before" 2>/dev/null || true

  # Save existing n8n config if exists
  [ -f "$N8N_DIR/docker-compose.yml" ] && cp "$N8N_DIR/docker-compose.yml" "$RESTORE_DIR/docker-compose.before" || true

  # Generate restore script
  cat > "$RESTORE_DIR/undo.sh" << 'UNDOEOF'
#!/usr/bin/env bash
# Restore script generated by deploy-agent-ops
set -e
RESTORE="$(cd "$(dirname "$0")" && pwd)"
echo "Restoring from: $RESTORE"

# Stop and remove n8n
if [ -f n8n-dir/docker-compose.yml ]; then
  cd n8n-dir && docker compose down -v 2>/dev/null || true
fi

# Restore cloudflared
[ -f "$RESTORE/cloudflared-config.before" ] && cp "$RESTORE/cloudflared-config.before" $HOME/.cloudflared/config.yml 2>/dev/null || true

# Restore crontab
[ -f "$RESTORE/crontab.before" ] && crontab "$RESTORE/crontab.before" 2>/dev/null || true

# Restore docker-compose
[ -f "$RESTORE/docker-compose.before" ] && cp "$RESTORE/docker-compose.before" . || true

echo "Restore complete. Review changes in $RESTORE"
echo "Run: rm -rf agent-ops  # to fully remove installation"
UNDOEOF

  chmod +x "$RESTORE_DIR/undo.sh"
  
  # Fix the n8n-dir placeholder in undo script
  sed -i "s|n8n-dir|$N8N_DIR|g" "$RESTORE_DIR/undo.sh"
  
  ok "Restore point saved. To undo: bash $RESTORE_DIR/undo.sh"
}

# ── Security Check ──────────────────────────────────────────
security_check() {
  header "Security Check"
  echo -e "  This script exposes services to the internet."
  echo -e "  ${YELLOW}May I check your system for common risks? (Y/N)${NC}"
  read -r answer < /dev/tty
  case "$answer" in
    [Yy]*)
      echo ""
      local issues=0

      # Check open ports
      if command -v ss &>/dev/null; then
        local listening=$(ss -tln | grep -cE "0\.0\.0\.0:|:::")
        if [ "$listening" -gt 10 ]; then
          warn "You have $listening open ports. Consider a firewall."
          issues=$((issues+1))
        else
          ok "Ports: $listening listening services — looks clean"
        fi
      fi

      # Check firewall
      if command -v ufw &>/dev/null; then
        local ufw_status=$(sudo ufw status 2>/dev/null | grep -c active || echo 0)
        if [ "$ufw_status" -eq 0 ]; then
          warn "UFW firewall is inactive. Consider enabling it."
          issues=$((issues+1))
        else
          ok "UFW firewall is active"
        fi
      elif command -v iptables &>/dev/null; then
        ok "iptables detected (checking rules manually not supported)"
      else
        warn "No firewall tool detected (ufw/iptables)"
      fi

      # Check Docker socket permissions
      if [ -S /var/run/docker.sock ]; then
        local sock_perms=$(stat -c '%a' /var/run/docker.sock 2>/dev/null || echo "unknown")
        if [ "$sock_perms" = "666" ] || [ "$sock_perms" = "777" ]; then
          warn "Docker socket is world-writable ($sock_perms). Anyone on this machine can run containers."
          issues=$((issues+1))
        else
          ok "Docker socket permissions: $sock_perms"
        fi
      fi

      # Check if root
      if [ "$EUID" = "0" ]; then
        warn "Running as root. Consider using a regular user with sudo."
        issues=$((issues+1))
      fi

      # Check passwordless sudo
      if sudo -n true 2>/dev/null; then
        warn "Passwordless sudo is enabled."
        issues=$((issues+1))
      fi

      # Check SSH config
      if [ -f /etc/ssh/sshd_config ]; then
        if grep -q 'PermitRootLogin yes' /etc/ssh/sshd_config 2>/dev/null; then
          warn "Root login via SSH is permitted."
          issues=$((issues+1))
        fi
        if grep -q 'PasswordAuthentication yes' /etc/ssh/sshd_config 2>/dev/null; then
          warn "Password authentication over SSH is enabled."
        fi
      fi

      echo ""
      if [ "$issues" -gt 0 ]; then
        warn "Found $issues potential issue(s). Review the warnings above."
      else
        ok "No obvious security issues found."
      fi

      echo ""
      echo -e "  ${YELLOW}Continue with installation? (Y/N)${NC}"
      read -r proceed < /dev/tty
      case "$proceed" in
        [Yy]*) ok "Proceeding with installation..." ;;
        *) echo -e "  ${RED}Installation cancelled by user.${NC}"; exit 0 ;;
      esac
      ;;
    *)
      warn "Skipped security check. Proceed at your own risk."
      echo -e "  ${YELLOW}Continue? (Y/N)${NC}"
      read -r proceed2 < /dev/tty
      case "$proceed2" in
        [Yy]*) ok "Proceeding..." ;;
        *) echo -e "  ${RED}Installation cancelled.${NC}"; exit 0 ;;
      esac
      ;;
  esac
}

# ── Requirements ──────────────────────────────────────────────
check_requirements() {
  header "Checking System Requirements"
  local pass=true

  if ! command -v docker &>/dev/null; then
    err "Docker not found. Installing..."
    curl -fsSL https://get.docker.com | bash
    sudo usermod -aG docker "$USER"
    warn "You may need to log out and back in for Docker group to take effect"
    ok "Docker installed"
  else
    ok "Docker: $(docker --version)"
  fi

  if ! docker compose version &>/dev/null && ! docker-compose --version &>/dev/null; then
    err "Docker Compose not found"
    exit 1
  else
    ok "Docker Compose: $(docker compose version 2>/dev/null || docker-compose --version 2>/dev/null)"
  fi

  if command -v python3 &>/dev/null; then
    ok "Python3: $(python3 --version)"
  else
    err "Python3 not found. Install it first."
    exit 1
  fi

  local mem=""
  if command -v free &>/dev/null; then
    mem=$(free -m | awk '/^Mem:/{print $2}')
  elif [ -f /proc/meminfo ]; then
    mem=$(awk '/MemTotal/{printf "%d", $2/1024}' /proc/meminfo)
  fi
  if [ -z "$mem" ] || [ "$mem" -eq 0 ] 2>/dev/null; then
    warn "Could not detect RAM"
  elif [ "$mem" -lt 4000 ]; then
    warn "Low RAM: ${mem}MB. n8n + agents may run slow (min 4GB recommended)"
  else
    ok "RAM: ${mem}MB"
  fi

  local disk=$(df -m "$HOME" | awk 'NR==2{print $4}')
  if [ "$disk" -lt 5120 ]; then
    warn "Low disk space: ${disk}MB. Need at least 5GB free"
  else
    ok "Disk: ${disk}MB free"
  fi
}

